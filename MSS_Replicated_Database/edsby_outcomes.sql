SELECT DISTINCT
	'*' AS SchoolID,
	'SK' AS REGION,
	CONCAT('SK.', CSK.CSK_COURSE_NUMBER) AS CourseID,
	'Document.LearningStandard' AS ItemType,
	RBC.RBC_COLUMN_HEADER AS 'Header',
	CASE WHEN RBC.RBC_DESCRIPTION IS NULL THEN '' ELSE RBC.RBC_DESCRIPTION END AS 'Description'
FROM 
	MSS_RUBRIC_CRITERION RBC
INNER JOIN
	MSS_RUBRIC_DEFINITION RBD ON RBC.RBC_RBD_OID = RBD.RBD_OID
INNER JOIN
	MSS_COURSE_SCHOOL CSK ON RBC.RBC_RBD_OID = CSK.CSK_RBD_OID
WHERE CSK.CSK_COURSE_NUMBER NOT IN 
